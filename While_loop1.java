//................Program to print 'Hello World!!' 100 times using while loop
package apna_college;

public class While_loop1 {
    public static void main(String[]args)
    {
        int i=1;
        while(i<=100)
        {
            System.out.print("Hello World!!");
            System.out.print("   ");
            i++;
        }
        
    }
    
}
