//....................Logical Operator........................
package apna_college;


public class Operator3 {
    public static void main(String[]args)
    {
        int a=5;
        int b=12;
        int c=13;
        int d=7;
        System.out.println((a>b)&&(c>d));
        System.out.println((a>b)||(c>d));
        System.out.println(!(a>b));
        System.out.println(!(a<b));
    }
    
}
