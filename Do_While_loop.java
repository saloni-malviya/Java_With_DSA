//...........Program to print "hello world" 10 times using do while loop............
package apna_college;

public class Do_While_loop {
    public static void main(String[]args)
    {
        int i=1;
        do{
          System.out.println("Hello World!!");
          i++;
    }while(i<=10);
}
}